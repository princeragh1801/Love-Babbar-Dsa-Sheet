class Solution
{
    public:
    Node* addOne(Node *head) 
    {
        // Your Code here
        // return head of list after adding one
        stack<Node*> st;
        Node *ptr = head;
        while (ptr)
        {
            st.push(ptr);
            ptr = ptr->next;
        }
        int carry = 0;
        ptr = st.top();
        ptr->data += 1;
        carry = ptr->data/10;
        ptr->data = ptr->data%10;
        st.pop();
        while(!st.empty()){
            ptr = st.top();
            ptr->data += carry;
            carry = ptr->data/10;
            ptr->data = ptr->data%10;
            st.pop();
        }
        head->data += (10*carry);
        return head;
        
    }
};

