// Tree traversal in recursive
struct Node
{
    int data;
    Node *left;
    Node *right;

    Node(int x){
        this->data = x;
        this->left = this->right = nullptr;
    }
};

void inorder(Node *root){
    if(root){
        inorder(root->left);
        cout<<root->data<<" ";
        inorder(root->right);
    }
}

void preorder(Node * root){
    if(root){
        cout<<root->data<<" ";
        preorder(root->left);
        preorder(root->right);
    }
}

void postorder(Node *root){
    if(root){
        postorder(root->left);
        postorder(root->right);
        cout<<root->data<<" ";
    }
}

