class Solution {
  public:
    int findPosition(int N) {
        // code here
        int i = 1;
        int pos = 1;
        while(i*2 <= N){
            i *= 2;
            pos++;
        }
        if(i == N){
            return pos;
        }
        return -1;
    }
};

