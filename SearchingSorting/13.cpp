class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        long long maximumLoot = INT_MIN;
        for(int i = 0; i < n; i++){
            long long s = 0;
            for(int j = i; j < n; j+=i+2){
                s += arr[j];
                maximumLoot = max(maximumLoot, s);
            }
           
        }
        return maximumLoot;
    }
};

